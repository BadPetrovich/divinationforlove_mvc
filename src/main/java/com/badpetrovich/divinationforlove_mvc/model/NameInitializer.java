package com.badpetrovich.divinationforlove_mvc.model;

public class NameInitializer {
    private String firstName, SecondName, ThirdName, FourthName;

    public NameInitializer() {
    }

    @Override
    public String toString() {
        return "NameInitializer{" +
                "firstName='" + firstName + '\'' +
                ", SecondName='" + SecondName + '\'' +
                ", ThirdName='" + ThirdName + '\'' +
                ", FourthName='" + FourthName + '\'' +
                '}';
    }
}
